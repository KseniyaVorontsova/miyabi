let currentLang = 'ru';

// Функция для обновления всех текстов на странице
function updateTexts(lang) {
  // Обновить все элементы с data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[key] && translations[key][lang]) {
      // Если элемент содержит HTML (например, <span class="gold">), используем innerHTML
      el.innerHTML = translations[key][lang];
    }
  });
  // Обновить lang у <html>
  document.documentElement.lang = lang === 'ru' ? 'ru' : 'ja';
  // Обновить текст переключателя языка
  const toggle = document.getElementById('lang-toggle');
  if (toggle) toggle.innerText = translations['lang_toggle'][lang];
  // Переключить класс на body для адреса
  if (lang === 'jp') {
    document.body.classList.add('lang-ja');
  } else {
    document.body.classList.remove('lang-ja');
  }
}

// Обработчик переключения языка
function toggleLang() {
  currentLang = currentLang === 'ru' ? 'jp' : 'ru';
  updateTexts(currentLang);
}

// Навесить обработчик на переключатель
window.addEventListener('DOMContentLoaded', () => {
  const toggle = document.getElementById('lang-toggle');
  if (toggle) toggle.addEventListener('click', toggleLang);
  // Инициализация текстов при загрузке
  updateTexts(currentLang);
});

function updateAddress(lang) {
  const addressEl = document.getElementById('contact-address');
  if (addressEl) {
    addressEl.innerHTML = translations[lang].contact_address_value;
  }
}
